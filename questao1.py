num = int(input(""))

def dia_semana():
    if num == 1:
        print("domingo")
    elif num == 2:
        print("segunda-feira")
    elif num == 3:
        print("terça-feira")
    elif num == 4:
        print("quarta-feira")
    elif num == 5:
        print("quinta-feira")
    elif num == 6:
        print("sexta-feira")
    elif num == 7:
        print("sábado")
    else:
        print("valor inválido")

dia_semana()
